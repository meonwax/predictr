package de.meonwax.predictr.settings;

public class Points {

    private Integer result;
    private Integer tendency;
    private Integer tendencySpread;

    public Integer getResult() {
        return result;
    }

    public void setResult(Integer result) {
        this.result = result;
    }

    public Integer getTendency() {
        return tendency;
    }

    public void setTendency(Integer tendency) {
        this.tendency = tendency;
    }

    public Integer getTendencySpread() {
        return tendencySpread;
    }

    public void setTendencySpread(Integer tendencySpread) {
        this.tendencySpread = tendencySpread;
    }
}
